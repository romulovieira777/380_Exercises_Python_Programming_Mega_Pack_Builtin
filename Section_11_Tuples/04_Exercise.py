"""
Exercise No. 04

The following tuple is given:

    default = ('YES', 'NO', 'NO', 'YES', 'NO')

Using the appropriate method, return the number of occurrences of the string 'YES' and print the result to the console
as shown below.

Expected result:

    Number of occurrences: 2
"""
default = ('YES', 'NO', 'NO', 'YES', 'NO')

print("Number of occurrences:", default.count('YES'))
print(f"Number of occurrences: {default.count('YES')}")
