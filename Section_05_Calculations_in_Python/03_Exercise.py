"""
Exercise No. 03

Write a program that calculates the delta for the quadratic equation:

    3x^2 - 4x + 1 = 0

Print the result to the console as shown below.

Expected result:

    Delta: 4
"""
quad_a = 3
quad_b = -4
quad_c = 1

delta = quad_b ** 2 - 4 * quad_a * quad_c

print(f'Delta: {delta}')
